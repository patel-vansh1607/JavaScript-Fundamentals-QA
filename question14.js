//What is the difference between logical operators && and ||?

// "&&" - its an AND operator and returns true if both conditions are true.
        //e.g
        let a = true;
        let b = false;

        console.log(a && b);  // - this will give false because one isnt true

// "||" - its an OR operator and returns true if at least one of the conditions is true.
        let a = true;
        let b = false;

        console.log(a|| b);  // - this will give true because atleast one is true